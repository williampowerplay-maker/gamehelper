// Skeleton row
function SkeletonRow() {
  return (
    <div className="flex items-center gap-2">
      <div className="w-4 h-4 rounded bg-[#2a2a3a] animate-pulse" />
      <div className="h-3 rounded bg-[#2a2a3a] animate-pulse" style={{ width: `${80 + Math.random() * 60}px` }} />
    </div>
  );
}

export default function CoverageStats() {
  const [stats, setStats] = useState<Stats | null>(null);
  const [error, setError] = useState(false);

  useEffect(() => {
    fetch("/api/coverage-stats")
      .then((r) => {
        if (!r.ok) throw new Error(`HTTP ${r.status}`);
        return r.json();
      })
      .then((data: Stats) => setStats(data))
      .catch(() => setError(true));
  }, []);

  // Error → hide entirely (don't show broken UI)
  if (error) return null;

  return (
    <div className="w-full max-w-md mx-auto mb-5">
      <p className="text-xs text-gray-500 mb-3 text-center">I can help you with:</p>

      {!stats ? (
        // Skeleton — 8 placeholder rows in a 2-col grid
        <div className="grid grid-cols-2 gap-x-6 gap-y-2.5">
          {Array.from({ length: 8 }).map((_, i) => (
            <SkeletonRow key={i} />
          ))}
        </div>
      ) : (
        <div className="grid grid-cols-2 gap-x-6 gap-y-2.5">
          {stats.categories.map(({ label, count }) => (
            <div key={label} className="flex items-center gap-2 min-w-0">
              <span className="text-red-500/80 flex-shrink-0">
                <CategoryIcon label={label} />
              </span>
              <span className="text-xs text-gray-400 truncate">
                {label}{" "}
                <span className="text-gray-600">({count.toLocaleString()})</span>
              </span>
            </div>
          ))}
        </div>
      )}

      <p className="text-xs text-gray-600 mt-3 text-center">
        If I don&apos;t have what you&apos;re looking for, I&apos;ll tell you directly.
      </p>
    </div>
  );
}
