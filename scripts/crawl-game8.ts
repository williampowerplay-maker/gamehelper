/**
 * Game8.co Crawler — supplements Fextralife wiki with guide/puzzle/mechanic content
 *
 * Crawls game8.co/games/Crimson-Desert guide pages and saves extracted text
 * directly into wiki-cache/ (same format as crawl-wiki.ts output),
 * so ingest-from-cache.ts picks them up without modification.
 *
 * Game8 covers content NOT in Fextralife:
 *   - Puzzle solutions (ancient ruins, strongbox, story puzzles)
 *   - Boss fight strategies (how to beat, weak points, phases)
 *   - Beginner tips, upgrade mechanics, system explanations
 *   - Item drop locations, build guides
 *
 * Usage:
 *   npx tsx scripts/crawl-game8.ts              # Full crawl (up to 800 pages)
 *   npx tsx scripts/crawl-game8.ts --dry-run    # Preview without writing
 *   npx tsx scripts/crawl-game8.ts --limit=200  # Cap page count
 *
 * Output: written to wiki-cache/pages/game8-{category}/ + appended to wiki-cache/manifest.json
 */

import * as fs from "fs";
import * as path from "path";
import * as crypto from "crypto";
import * as https from "https";

// ===== CONFIG =====
const BASE_URL = "https://game8.co";
const GAME_PREFIX = "/games/Crimson-Desert";

// Write into the same wiki-cache so ingest-from-cache.ts picks it up automatically
const CACHE_DIR = path.join(__dirname, "..", "wiki-cache");
const PAGES_DIR = path.join(CACHE_DIR, "pages");
const MANIFEST_PATH = path.join(CACHE_DIR, "manifest.json");

const DELAY_MS = 1000; // polite rate limit
const limitArg = process.argv.find((a) => a.startsWith("--limit="));
const MAX_PAGES = parseInt(limitArg?.split("=")[1] ?? "800");
const DRY_RUN = process.argv.includes("--dry-run");

const sleep = (ms: number) => new Promise((r) => setTimeout(r, ms));

// ===== SEED PAGES =====
// Game8 renders links via JavaScript, so BFS doesn't work.
// Instead we load all known archive IDs from game8_ids.json (generated by
// running: node -e "..." against the game8 hub page).
